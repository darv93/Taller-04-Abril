public class Chasis {
    private int id;
    private String clase;

    public Chasis(){
    
    }
    public Chasis(int id, String clase) {
        this.id = id;
        this.clase = clase;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getClase() {
        return clase;
    }

    public void setClase(String clase) {
        this.clase = clase;
    }
    
    
}
